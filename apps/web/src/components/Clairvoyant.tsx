const CANVAS_WIDTH = 561.6;
const CANVAS_HEIGHT = 162;
const CELL_WIDTH = 10.8;
const CELL_HEIGHT = 18;
const FONT_SIZE = 18;
const FONT_FAMILY =
	"SF Mono, Monaco, Cascadia Code, Consolas, JetBrains Mono, Fira Code, Monaspace Neon, Geist Mono, Courier New, monospace";
const BACKGROUND_COLOR = null;

const ClairvoyantMobile = (props: AsciiMotionComponentProps = {}) => {
	const { showControls = true, autoPlay = true, onReady } = props;
	const initialAutoPlay = autoPlay !== false;
	const canvasRef = useRef<HTMLCanvasElement | null>(null);
	const animationFrameRef = useRef<number | null>(null);
	const frameIndexRef = useRef<number>(0);
	const frameElapsedRef = useRef<number>(0);
	const lastTimestampRef = useRef<number>(0);
	const restartRef = useRef<() => void>(() => {});
	const isPlayingRef = useRef<boolean>(initialAutoPlay);
	const updatePlayingState = useCallback((value: boolean) => {
		isPlayingRef.current = value;
	}, []);
	const play = useCallback(() => {
		updatePlayingState(true);
	}, [updatePlayingState]);
	const pause = useCallback(() => {
		updatePlayingState(false);
	}, [updatePlayingState]);
	const togglePlay = useCallback(() => {
		updatePlayingState(!isPlayingRef.current);
	}, [updatePlayingState]);
	const restart = useCallback(() => {
		if (restartRef.current) {
			restartRef.current();
		}
	}, []);

	useEffect(() => {
		if (isPlayingRef.current !== initialAutoPlay) {
			updatePlayingState(initialAutoPlay);
		}
	}, [initialAutoPlay, updatePlayingState]);

	useEffect(() => {
		const canvas = canvasRef.current;
		if (!canvas) {
			return;
		}

		const context = canvas.getContext("2d");
		if (!context) {
			return;
		}

		const devicePixelRatio = window.devicePixelRatio || 1;
		canvas.width = CANVAS_WIDTH * devicePixelRatio;
		canvas.height = CANVAS_HEIGHT * devicePixelRatio;
		canvas.style.width = CANVAS_WIDTH + "px";
		canvas.style.height = CANVAS_HEIGHT + "px";
		context.resetTransform();
		context.scale(devicePixelRatio, devicePixelRatio);
		context.textAlign = "center";
		context.textBaseline = "middle";
		context.font = FONT_SIZE + "px " + FONT_FAMILY;
		context.imageSmoothingEnabled = false;

		frameIndexRef.current = 0;
		frameElapsedRef.current = 0;
		lastTimestampRef.current = 0;

		const drawFrame = (index: number) => {
			const frame = FRAMES[index];

			if (BACKGROUND_COLOR) {
				context.fillStyle = BACKGROUND_COLOR;
				context.fillRect(0, 0, CANVAS_WIDTH, CANVAS_HEIGHT);
			} else {
				context.clearRect(0, 0, CANVAS_WIDTH, CANVAS_HEIGHT);
			}

			if (!frame) {
				return;
			}

			for (const cell of frame.cells) {
				if (cell.bgColor) {
					context.fillStyle = cell.bgColor;
					context.fillRect(
						cell.x * CELL_WIDTH,
						cell.y * CELL_HEIGHT,
						CELL_WIDTH,
						CELL_HEIGHT,
					);
				}

				context.fillStyle = cell.color || "#ffffff";
				context.fillText(
					cell.char,
					cell.x * CELL_WIDTH + CELL_WIDTH / 2,
					cell.y * CELL_HEIGHT + CELL_HEIGHT / 2,
				);
			}
		};

		drawFrame(frameIndexRef.current);

		if (FRAMES.length === 0) {
			restartRef.current = () => {
				drawFrame(0);
			};
			return;
		}

		const step = (timestamp: number) => {
			if (FRAMES.length === 0) {
				return;
			}

			if (lastTimestampRef.current === 0) {
				lastTimestampRef.current = timestamp;
			}

			const delta = timestamp - lastTimestampRef.current;
			lastTimestampRef.current = timestamp;

			if (isPlayingRef.current) {
				// Stop if we're already at the last frame
				if (frameIndexRef.current >= FRAMES.length - 1) {
					updatePlayingState(false);
					drawFrame(frameIndexRef.current);
					return;
				}

				frameElapsedRef.current += delta;

				let nextIndex = frameIndexRef.current;
				let remaining = frameElapsedRef.current;
				let duration = FRAMES[nextIndex]?.duration ?? 16;

				while (remaining >= duration && FRAMES.length > 0) {
					remaining -= duration;
					nextIndex = nextIndex + 1;

					// Stop at the last frame instead of looping
					if (nextIndex >= FRAMES.length) {
						nextIndex = FRAMES.length - 1;
						updatePlayingState(false);
						break;
					}

					duration = FRAMES[nextIndex]?.duration ?? duration;
				}

				frameElapsedRef.current = remaining;

				if (nextIndex !== frameIndexRef.current) {
					frameIndexRef.current = nextIndex;
					drawFrame(nextIndex);
				} else {
					drawFrame(frameIndexRef.current);
				}
			} else {
				drawFrame(frameIndexRef.current);
			}

			animationFrameRef.current = window.requestAnimationFrame(step);
		};

		animationFrameRef.current = window.requestAnimationFrame(step);

		restartRef.current = () => {
			frameIndexRef.current = 0;
			frameElapsedRef.current = 0;
			lastTimestampRef.current = 0;
			drawFrame(0);
		};

		return () => {
			if (animationFrameRef.current !== null) {
				window.cancelAnimationFrame(animationFrameRef.current);
				animationFrameRef.current = null;
			}
		};
	}, [updatePlayingState]);

	useEffect(() => {
		if (typeof onReady === "function") {
			onReady({
				play,
				pause,
				togglePlay,
				restart,
			});
		}
	}, [onReady, play, pause, togglePlay, restart]);
	return (
		<div
			style={{
				display: "inline-flex",
				flexDirection: "column",
				alignItems: "center",
			}}
		>
			<canvas
				ref={canvasRef}
				width={CANVAS_WIDTH}
				height={CANVAS_HEIGHT}
				style={{
					width: CANVAS_WIDTH + "px",
					height: CANVAS_HEIGHT + "px",
					backgroundColor: BACKGROUND_COLOR || "transparent",
					imageRendering: "pixelated",
				}}
			/>
		</div>
	);
};

export default ClairvoyantMobile;
